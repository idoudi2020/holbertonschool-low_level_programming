#include "3-calc.h"

/**
 * op_add - function add 2 int
 * @a: first int
 * @b: second int
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
  return (a + b);
}

/**
 * op_sub - function subtraction
 * @a: first int
 * @b: second int
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
  return (a - b);
}

/**
 * op_mul - function multiple two int
 * @a: first int
 * @b: second int
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
  return (a * b);
}

/**
 * op_div - function divide int a by b
 * @a: first int
 * @b: second int
 * Return: the result of the division of a by b
 */
int op_div(int a, int b)
{
  return (a / b);
}

/**
 * op_mod - find the remain of division of a by b
 * @a: first int
 * @b: second int
 * Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
  return (a % b);
}
